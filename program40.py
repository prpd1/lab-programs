a = input("enter alphabet:")

if a == 'a' or a == 'e' or a == 'i'or  a == 'o' or a == 'u' or a == 'A' or a == 'E' or a == 'I'or  a == 'O' or a == 'U':
    print("{} is vowel".format(a))

else :
    print("{} is consonant".format(a))
 
 